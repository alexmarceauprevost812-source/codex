export function EmojiPicker({
  open,
  anchorRef,
  onClose,
  onSelect,
}: {
  open: boolean;
  anchorRef: RefObject<HTMLElement | null>;
  onClose: () => void;
  onSelect: (emoji: string) => void;
}) {
  const [activeId, setActiveId] = useState(CATEGORIES[0].id);
  const popoverRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!open) return;
    function onDocPointer(event: PointerEvent) {
      const target = event.target as Node;
      if (popoverRef.current?.contains(target)) return;
      if (anchorRef.current?.contains(target)) return;
      onClose();
    }
    function onKey(event: KeyboardEvent) {
      if (event.key === "Escape") onClose();
    }
    document.addEventListener("pointerdown", onDocPointer);
    document.addEventListener("keydown", onKey);
    return () => {
      document.removeEventListener("pointerdown", onDocPointer);
      document.removeEventListener("keydown", onKey);
    };
  }, [open, anchorRef, onClose]);

  if (!open) return null;

  const active = CATEGORIES.find((c) => c.id === activeId) ?? CATEGORIES[0];

  return (
    <div
      ref={popoverRef}
      role="dialog"
      aria-label="Sélecteur d'emoji"
      className="absolute bottom-full left-0 right-0 z-30 mb-2 mx-auto max-w-[min(22rem,calc(100vw-1rem))] overflow-hidden rounded-2xl border border-[var(--border-soft)] bg-[var(--modal-surface)] shadow-2xl"
    >
      <div className="flex gap-0.5 overflow-x-auto border-b border-[var(--border-soft)] bg-[var(--soft-surface)] p-1">
        {CATEGORIES.map((c) => (
          <button
            key={c.id}
            type="button"
            onClick={() => setActiveId(c.id)}
            aria-label={c.label}
            title={c.label}
            aria-pressed={c.id === activeId}
            className={`flex-shrink-0 rounded-lg px-2 py-1.5 text-lg leading-none transition ${
              c.id === activeId
                ? "bg-[var(--accent)]/20"
                : "hover:bg-[var(--user-bubble)]"
            }`}
          >
            <span style={{ fontFamily: '"Apple Color Emoji","Segoe UI Emoji","Segoe UI Symbol","Noto Color Emoji",sans-serif' }}>
              {c.icon}
            </span>
          </button>
        ))}
      </div>
      <div className="max-h-64 overflow-y-auto p-1.5">
        <p className="px-2 pb-1 pt-0.5 text-[10px] uppercase tracking-wider text-[var(--fg-50)]">
          {active.label}
        </p>
        <div className="grid grid-cols-8 gap-0.5">
          {active.emojis.map((e, i) => (
            <button
              key={`${active.id}-${i}-${e}`}
              type="button"
              onClick={() => onSelect(e)}
              aria-label={e}
              className="aspect-square rounded text-xl leading-none transition hover:bg-[var(--user-bubble)]"
            >
              <span style={{ fontFamily: '"Apple Color Emoji","Segoe UI Emoji","Segoe UI Symbol","Noto Color Emoji",sans-serif' }}>
                {e}
              </span>
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}
